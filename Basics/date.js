let date = new Date();
console.log(date); // 2021-07-07T07:00:00.000Z

console.log(date.toString()); // Wed Jul 07 2021 12:30:00 GMT+0530 (India Standard Time)

console.log(date.toDateString()); // Wed Jul 07 2021

console.log(date.toTimeString()); // 12:30:00 GMT+0530 (India Standard Time)

console.log(date.toLocaleString()); // 7/7/2021, 12:30:00 PM

console.log(date.getFullYear()); // 2024

console.log(date.getMonth()); // 6

console.log(date.getDate()); // 7

console.log(date.getDay()); // 3

